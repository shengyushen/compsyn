		| T_bit_or2(exp1,exp2) -> begin
			let (vec1,clslst1)=self#encode_exp exp1
			and (vec2,clslst2)=self#encode_exp exp2
			in begin
				if ((List.length vec1) == (List.length vec2) ) then begin
					let pl=List.combine vec1 vec2
