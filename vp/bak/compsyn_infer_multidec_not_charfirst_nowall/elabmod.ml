				b := (!b)+1 ;
				xxx := self#check_p_r_l (!b) (!b) (!b) (!infered_assertion_array_lst_old);
				match (!xxx) with
				(res1,p1,l1,r1,infered_assertion_array_lst_old1) -> begin
