				if(isTcIn ztc)=false then begin
					addTcIn (TYPE_GFDATA_BOOL(ztc));
				end;
				if(isTcIn atc)=false then begin
					addTcIn (TYPE_GFDATA_BOOL(atc));
				end;
			end
